public class suma {
    public class SumarDosNumeros {

        public static void main(String[] args) {
            // Declaramos las variables
            int numero1 = 10;
            int numero2 = 20;
    
            // Realizamos la suma
            int suma = numero1 + numero2;
            int resta = numero1 - numero2;
            int multiplicar = numero1 * numero2;
    
            // Imprimimos el resultado
            System.out.println("La suma de los números " + numero1 + " y " + numero2 + " es " + suma);
            System.out.println("La resta de los números " + numero1 + " y " + numero2 + " es " + resta);
            System.out.println("La multiplicacion de los números " + numero1 + " y " + numero2 + " es " + multiplicar);
        }
    }
    
}
